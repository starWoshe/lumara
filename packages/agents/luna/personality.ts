// Типи запитів для визначення промпту
export type LunaQueryType = 'natal' | 'transits' | 'synastry' | 'forecast' | 'daily' | 'general'

// Ключові слова для визначення типу запиту
export const QUERY_KEYWORDS: Record<LunaQueryType, string[]> = {
  natal: ['натальн', 'натал', 'natal', 'народження', 'карта', 'chart', 'гороскоп народження', 'аскендант', 'ascendant'],
  transits: ['транзит', 'transit', 'зараз', 'сьогодні', 'поточн', 'наразі', 'актуальн', 'планети сьогодні'],
  synastry: ['сумісність', 'синастрія', 'synastry', 'партнер', 'стосунки', 'кохання', 'пара', 'compatibility'],
  forecast: ['прогноз', 'forecast', 'майбутн', 'що буде', 'перспектив', 'планування', 'місяць', 'рік'],
  daily: ['день', 'daily', 'щоденн', 'тижень', 'week', 'ранок', 'вечір', 'horoscope'],
  general: [],
}

// Визначення типу запиту за текстом
export function detectQueryType(content: string): LunaQueryType {
  const lower = content.toLowerCase()

  for (const [type, keywords] of Object.entries(QUERY_KEYWORDS) as [LunaQueryType, string[]][]) {
    if (type === 'general') continue
    if (keywords.some((kw) => lower.includes(kw))) {
      return type
    }
  }

  return 'general'
}
